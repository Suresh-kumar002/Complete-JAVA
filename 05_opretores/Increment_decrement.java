public class Increment_decrement {
    public static void main(String[] args) {
        int a=10;
        //                             increment
        System.out.println(a++);
        System.out.println(a++);
        System.out.println(a);
        System.out.println(++a);
        System.out.println(a);
        
        System.out.println();
        //                             decrement
        System.out.println(a--);
        System.out.println(a--);
        System.out.println(a);
        System.out.println(--a);
        System.out.println(a);

    }

}
